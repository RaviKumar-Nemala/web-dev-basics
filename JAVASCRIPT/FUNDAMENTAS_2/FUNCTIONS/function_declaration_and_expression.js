
// function declarations 

function add (  num1 , num2 )
{
     return   ( num1 + num2   ); 
     
}

console.log ( add ( 1 , 2 ) ) 



// function expressions 
const  add_values = function ( num1 , num2)
{
     return  ( num1 + num2 ) ;
}


console.log( add_values( 1, 5 ) )   
/* 

function declarations allows to call the function before the function declaration 

function expressions does no allow to call the function before the function expression 


*/





